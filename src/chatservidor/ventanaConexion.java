    private void bEmpezarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bEmpezarActionPerformed

        if(ventanaChat == null){
            //JOptionPane option = new JOptionPane("Estableciendo conexión...", JOptionPane.INFORMATION_MESSAGE, JOptionPane.CANCEL_OPTION);
            //JDialog dialogo = option.createDialog(this, "Conectando...");
            //dialogo.setVisible(true);
            ventanaChat = new VentanaChat(puerto, this);
            ventanaChat.setVisible(true);
        }else{
            try{
                ventanaChat.chat.EstablecerConexion(puerto);
            }catch(UnknownHostException e){
            }catch(IOException e){
            }catch(AWTException e){
            }catch(ClassNotFoundException e){}
        }
        ventanaChat.setVisible(true);
        this.setVisible(false);
    }//GEN-LAST:event_bEmpezarActionPerformed

