    private void bAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bAceptarActionPerformed
        if(!fieldPuerto.getText().equals("")){
            puerto = Integer.parseInt(fieldPuerto.getText());
            labelPuertoActual.setText("Puerto actual: " + puerto);
            fieldPuerto.setText("");
            try {
                if (ventanaChat != null) {
                    ventanaChat.chat.CerrarConexion();
                }
            } catch (IOException e) {
            }
        }
        dialogoPuerto.setVisible(false);
    }//GEN-LAST:event_bAceptarActionPerformed

